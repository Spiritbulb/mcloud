// scripts/build-manifest.mjs
// Globs themes/**/*.liquid into src/themes-manifest.ts so templates are bundled
// as strings (no runtime disk access). Re-run whenever a .liquid file changes.
import fs from 'fs'
import path from 'path'
import { glob } from 'glob'
import { fileURLToPath } from 'url'

const dir = path.dirname(fileURLToPath(import.meta.url))
const root = path.resolve(dir, '..')
const themesRoot = path.join(root, 'themes')
const outputFile = path.join(root, 'src', 'themes-manifest.ts')

fs.mkdirSync(path.dirname(outputFile), { recursive: true })

const files = glob.sync('**/*.liquid', { cwd: themesRoot })
const entries = files.map((file) => {
    const key = file.replace(/\.liquid$/, '').replace(/\\/g, '/')
    const content = fs.readFileSync(path.join(themesRoot, file), 'utf-8')
    return `  ${JSON.stringify(key)}: ${JSON.stringify(content)},`
})

const output = `// AUTO-GENERATED by scripts/build-manifest.mjs — do not edit manually\nexport const themeFiles: Record<string, string> = {\n${entries.join('\n')}\n};\n`
fs.writeFileSync(outputFile, output)
console.log(`Generated themes-manifest with ${files.length} templates.`)
